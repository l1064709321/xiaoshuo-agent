    def audit(self, text, outline=None):
        """对文本进行33维审计，返回审计报告。"""
        report = {"total": len(self.dimensions), "issues": [], "highlights": [], "scores": {}}
        for dim in self.dimensions:
            try:
                result = dim["check"](text, outline)
                if result:
                    result["dimension"] = dim["name"]
                    result["category"] = dim["cat"]
                    result["id"] = dim["id"]
                    if result.get("level") == "highlight":
                        report["highlights"].append(result)
                    else:
                        report["issues"].append(result)
            except Exception as e:
                pass  # 某些检查可能因文本太短而跳过
        # 统计
        cat_scores = {}
        for dim in self.dimensions:
            cat = dim["cat"]
            if cat not in cat_scores:
                cat_scores[cat] = {"total": 0, "issues": 0}
            cat_scores[cat]["total"] += 1
        for issue in report["issues"]:
            cat = issue.get("category", "")
            if cat in cat_scores:
                cat_scores[cat]["issues"] += 1
        for cat, data in cat_scores.items():
            data["score"] = round((1 - data["issues"] / max(data["total"], 1)) * 100)
        report["scores"] = cat_scores
        return report

