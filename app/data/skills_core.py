    """专门检测和清洗AI味文本。基于公开研究（stop-slop-zh、搜狐/知乎AI写作分析）。"""

    PATTERNS = [
        # === 套话连接词（AI最爱的废话） ===
        (r"综上所述", "critical", "AI最爱的总结套话", "直接删掉，不需要总结"),
        (r"值得注意的是", "critical", "AI最爱的过渡套话", "直接删掉，用具体信息替代"),
        (r"不难发现", "warning", "AI的判断套话", "删掉，直接说发现"),
        (r"换句话说", "warning", "AI的解释套话", "删掉，直接说"),
        (r"让我们拭目以待", "critical", "AI最爱的结尾套话", "删掉，用具体行动或悬念收尾"),
        (r"在当今.{2,15}的时代", "critical", "AI最爱的开头套话", "删掉，直接切入主题"),
        (r"随着.{2,15}的不断(?:进步|发展|深入)", "critical", "AI的时代背景套话", "删掉，用具体案例替代"),
        (r"越来越多的", "warning", "AI的数据模糊化表达", "用具体数字替代"),
        (r"与此同时", "warning", "AI最爱的连接词", "删掉或用'这时'替代"),
        (r"不可忽视的", "warning", "AI的强调套话", "删掉，直接说重要"),

        # === 排比三件套 ===
        (r"首先[，,].{5,30}其次[，,].{5,30}最后", "critical", "AI的排比三件套", "打散结构，不要一二三"),
        (r"一方面.{5,30}另一方面", "warning", "AI的对比套话", "用具体案例替代"),

        # === 金句收尾（AI最爱的结尾方式） ===
        (r"(?:这|那).{0,5}(?:不仅|不只).{5,30}更是", "warning", "AI的递进金句", "删掉，用行动收尾"),
        (r"(?:这|那).{0,5}才是.{2,15}(?:真正|本质)", "warning", "AI的升华金句", "删掉，让读者自己感悟"),

        # === 真正的AI味句式（之前遗漏的） ===
        (r"他(?:的)?心中不由得.{2,6}(?:一惊|一震|一沉|一紧)", "critical", "AI最爱的内心独白模板", "删除'不由得'，直接写动作"),
        (r"一股(?:暖流|寒意|热流)涌上(?:心头|心间|全身)", "critical", "AI的情绪描写模板", "用行动替代感受"),
        (r"不由自主地", "critical", "AI的高频词", "删除，直接写动作"),
        (r"这一切.{0,5}(?:说来复杂|看似复杂).{0,10}实则", "critical", "AI的过渡废话", "直接删掉"),
    ]

    def detect(self, text):
        """检测文本中的AI味句式，返回问题列表。"""
        import re
        issues = []
        for pattern, level, reason, suggestion in self.PATTERNS:
            matches = re.findall(pattern, text)
            if matches:
                issues.append({
                    "pattern": pattern,
                    "level": level,
                    "count": len(matches),
                    "example": matches[0][:30] + "..." if len(matches[0]) > 30 else matches[0],
                    "reason": reason,
                    "suggestion": suggestion,
                })
        return issues

    def format_report(self, issues):
        """格式化AI味检测报告。"""
        if not issues:
            return "✅ 未检测到明显AI味，文本质量良好。"
        lines = ["=" * 50, "  🤖 AI味检测报告", "=" * 50, ""]
        critical = [i for i in issues if i["level"] == "critical"]
        warning = [i for i in issues if i["level"] == "warning"]
        info = [i for i in issues if i["level"] == "info"]
        if critical:
            lines.append("🔴 必须修改（强烈AI味）:")
            for issue in critical:
                lines.append(f"  ✖ 「{issue['example']}」出现{issue['count']}次")
                lines.append(f"    原因: {issue['reason']}")
                lines.append(f"    修改: {issue['suggestion']}")
                lines.append("")
        if warning:
            lines.append("🟡 建议修改（中等AI味）:")
            for issue in warning:
                lines.append(f"  ⚠ 「{issue['example']}」出现{issue['count']}次")
                lines.append(f"    原因: {issue['reason']}")
                lines.append(f"    修改: {issue['suggestion']}")
                lines.append("")
        if info:
            lines.append("🟢 轻微（可优化）:")
            for issue in info:
                lines.append(f"  ℹ 「{issue['example']}」出现{issue['count']}次")
                lines.append(f"    建议: {issue['suggestion']}")
            lines.append("")
        total = sum(i["count"] for i in issues)
        lines.append(f"共检测到 {total} 处AI味句式，{len(critical)}处严重，{len(warning)}处中等，{len(info)}处轻微。")
        return "\n".join(lines)


